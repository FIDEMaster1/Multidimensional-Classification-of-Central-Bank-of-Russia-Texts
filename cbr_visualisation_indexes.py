# Date parsing utilities
def parse_date_unified(published_at):
    """Parse date from various formats: '13 февраля 2026 г' or '20.03.2026'"""
    if not published_at:
        return None

    months_ru = {
        'января': 1, 'февраля': 2, 'марта': 3, 'апреля': 4,
        'мая': 5, 'июня': 6, 'июля': 7, 'августа': 8,
        'сентября': 9, 'октября': 10, 'ноября': 11, 'декабря': 12
    }

    try:
        # Format: '13 февраля 2026 г'
        match = re.search(r'(\d+)\s+(\w+)\s+(\d{4})', published_at)
        if match:
            day, month_name, year = match.groups()
            month = months_ru.get(month_name.lower())
            if month:
                return f"{year}-{month:02d}-{int(day):02d}"

        # Format: '20.03.2026' or '20-03-2026'
        match = re.search(r'(\d{2})[.\-/](\d{2})[.\-/](\d{4})', published_at)
        if match:
            day, month, year = match.groups()
            return f"{year}-{int(month):02d}-{int(day):02d}"

        # Format: '2026.03.20'
        match = re.search(r'(\d{4})[.\-/](\d{2})[.\-/](\d{2})', published_at)
        if match:
            year, month, day = match.groups()
            return f"{year}-{int(month):02d}-{int(day):02d}"

    except Exception as e:
        print(f"Date parsing error '{published_at}': {e}")
        return None

    return None

