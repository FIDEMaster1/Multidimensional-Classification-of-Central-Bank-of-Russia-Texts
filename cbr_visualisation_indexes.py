def clean_numeric_series(s: pd.Series) -> pd.Series:
    """Convert strings like '13 024,8' to float"""
    return (
        s.astype(str)
         .str.replace("\xa0", "", regex=False)
         .str.replace(" ", "", regex=False)
         .str.replace(",", ".", regex=False)
         .replace({"nan": np.nan, "None": np.nan, "": np.nan})
         .astype(float)
    )

def quarter_to_month_end(year: int, quarter_roman: str) -> pd.Timestamp:
    """Convert quarter notation to month end: 'I квартал' -> 03-31"""
    q_map = {"I": 3, "II": 6, "III": 9, "IV": 12}
    month = q_map[quarter_roman]
    return pd.Timestamp(year=year, month=month, day=1) + pd.offsets.MonthEnd(0)

def detect_sheet_by_index_or_name(xlsx_path, preferred_index: int = 2) -> str:
    """Return sheet name, default to 3rd sheet"""
    xl = pd.ExcelFile(xlsx_path)
    if len(xl.sheet_names) > preferred_index:
        return xl.sheet_names[preferred_index]
    return xl.sheet_names[0]

def load_gdp_quarterly(gdp_file, sheet_name: str | None = None) -> pd.DataFrame:
    """Load quarterly GDP data and convert to standardized format"""
    if sheet_name is None:
        sheet_name = detect_sheet_by_index_or_name(gdp_file, preferred_index=2)

    raw = pd.read_excel(gdp_file, sheet_name=sheet_name, header=None)
    raw = raw.dropna(axis=0, how="all").dropna(axis=1, how="all").reset_index(drop=True)

    # Find year row
    year_row_idx = None
    for i in range(min(10, len(raw))):
        row_vals = raw.iloc[i].astype(str).tolist()
        year_count = sum(bool(re.fullmatch(r"\d{4}", v.strip())) for v in row_vals if v.strip() != "nan")
        if year_count >= 2:
            year_row_idx = i
            break

    if year_row_idx is None:
        raise ValueError("Could not find year row in GDP file")

    quarter_row_idx = year_row_idx + 1
    value_row_idx = quarter_row_idx + 1

    years = raw.iloc[year_row_idx].copy().ffill()
    quarters = raw.iloc[quarter_row_idx].copy()
    values = raw.iloc[value_row_idx].copy()

    records = []
    for y, q, v in zip(years, quarters, values):
        if pd.isna(y) or pd.isna(q) or pd.isna(v):
            continue

        y_str = str(y).strip()
        q_str = str(q).strip()

        m = re.match(r"(I{1,3}|IV)\s+квартал", q_str, flags=re.IGNORECASE)
        if not re.fullmatch(r"\d{4}", y_str) or m is None:
            continue

        quarter_roman = m.group(1).upper()
        dt = quarter_to_month_end(int(y_str), quarter_roman)

        records.append({"date": dt, "gdp_real_sa": v})

    if not records:
        raise ValueError("Could not parse quarterly GDP values")

    gdp = pd.DataFrame(records)
    gdp["gdp_real_sa"] = clean_numeric_series(gdp["gdp_real_sa"])
    gdp = gdp.sort_values("date").drop_duplicates("date").reset_index(drop=True)

    # Calculate derivatives
    gdp["gdp_qoq"] = gdp["gdp_real_sa"].pct_change()
    gdp["gdp_yoy"] = gdp["gdp_real_sa"].pct_change(4)

    return gdp

def convert_quarterly_to_monthly_ffill(gdp_q: pd.DataFrame) -> pd.DataFrame:
    """Convert quarterly GDP to monthly using forward fill"""
    monthly_index = pd.date_range(
        start=gdp_q["date"].min().to_period("M").to_timestamp("M"),
        end=gdp_q["date"].max().to_period("M").to_timestamp("M"),
        freq="ME",
    )

    gdp_m = gdp_q.set_index("date").reindex(monthly_index).rename_axis("date").sort_index()
    gdp_m[["gdp_real_sa", "gdp_qoq", "gdp_yoy"]] = gdp_m[["gdp_real_sa", "gdp_qoq", "gdp_yoy"]].ffill()
    gdp_m = gdp_m.reset_index()

    return gdp_m

def load_ruonia_daily(ruonia_file) -> pd.DataFrame:
    """Load daily RUONIA rate data"""
    df = pd.read_excel(ruonia_file)
    df.columns = [str(c).strip() for c in df.columns]

    if "DT" not in df.columns:
        raise ValueError("DT column not found in RUONIA file")

    df["DT"] = pd.to_datetime(df["DT"], dayfirst=True, errors="coerce")

    numeric_cols = ["ruo", "vol", "MinRate", "Percentile25", "Percentile75", "MaxRate"]
    for col in numeric_cols:
        if col in df.columns:
            df[col] = clean_numeric_series(df[col])

    df = df.dropna(subset=["DT"]).sort_values("DT").reset_index(drop=True)
    return df

def aggregate_ruonia_monthly(ruonia_daily: pd.DataFrame) -> pd.DataFrame:
    """Aggregate daily RUONIA to monthly"""
    agg_map = {}
    if "ruo" in ruonia_daily.columns:
        agg_map["ruo"] = ["mean", "last"]
    if "vol" in ruonia_daily.columns:
        agg_map["vol"] = ["mean", "sum"]
    if "MinRate" in ruonia_daily.columns:
        agg_map["MinRate"] = "mean"
    if "Percentile25" in ruonia_daily.columns:
        agg_map["Percentile25"] = "mean"
    if "Percentile75" in ruonia_daily.columns:
        agg_map["Percentile75"] = "mean"
    if "MaxRate" in ruonia_daily.columns:
        agg_map["MaxRate"] = "mean"

    monthly = ruonia_daily.set_index("DT").resample("ME").agg(agg_map)

    monthly.columns = ["_".join([c for c in col if c]).strip("_") if isinstance(col, tuple) else col for col in monthly.columns]
    monthly = monthly.reset_index().rename(columns={"DT": "date"})

    rename_map = {
        "ruo_mean": "ruonia_mean",
        "ruo_last": "ruonia_last",
        "vol_mean": "ruonia_volume_mean",
        "vol_sum": "ruonia_volume_sum",
    }
    monthly = monthly.rename(columns=rename_map)

    return monthly

def load_rate_inflation_monthly(rate_infl_file) -> pd.DataFrame:
    """Load monthly key rate and inflation data"""
    df = pd.read_excel(rate_infl_file)
    df.columns = [str(c).strip() for c in df.columns]

    date_col = None
    for c in df.columns:
        if "дата" in c.lower():
            date_col = c
            break
    if date_col is None:
        raise ValueError("Date column not found in rate/inflation file")

    rate_col = next((c for c in df.columns if "ключевая" in c.lower()), None)
    infl_col = next((c for c in df.columns if "инфляц" in c.lower()), None)
    target_col = next((c for c in df.columns if "цель" in c.lower()), None)

    if rate_col is None or infl_col is None:
        raise ValueError("Key rate or inflation columns not found")

    df["date"] = pd.to_datetime("01." + df[date_col].astype(str), format="%d.%m.%Y", errors="coerce")
    df["date"] = df["date"] + pd.offsets.MonthEnd(0)

    df["key_rate"] = clean_numeric_series(df[rate_col])
    df["inflation_yoy"] = clean_numeric_series(df[infl_col])

    if target_col is not None:
        df["inflation_target"] = clean_numeric_series(df[target_col])
    else:
        df["inflation_target"] = np.nan

    out = df[["date", "key_rate", "inflation_yoy", "inflation_target"]].dropna(subset=["date"])
    out = out.sort_values("date").drop_duplicates("date").reset_index(drop=True)

    return out

def load_fx_daily(fx_file) -> pd.DataFrame:
    """Load daily exchange rate data"""
    df = pd.read_excel(fx_file)
    df.columns = [str(c).strip() for c in df.columns]

    date_col = next((c for c in df.columns if c.lower() in {"data", "date"}), None)
    rate_col = next((c for c in df.columns if "curs" in c.lower()), None)
    cdx_col = next((c for c in df.columns if "cdx" in c.lower()), None)

    if date_col is None or rate_col is None:
        raise ValueError("Date or rate column not found in FX file")

    df[date_col] = pd.to_datetime(df[date_col], dayfirst=True, errors="coerce")
    df[rate_col] = clean_numeric_series(df[rate_col])

    # Filter for USD if multiple currencies present
    if cdx_col is not None:
        mask = df[cdx_col].astype(str).str.contains("доллар", case=False, na=False)
        if mask.any():
            df = df.loc[mask].copy()

    out = df[[date_col, rate_col]].rename(columns={date_col: "date", rate_col: "usd_rub"})
    out = out.dropna(subset=["date", "usd_rub"]).sort_values("date").reset_index(drop=True)

    return out

def aggregate_fx_monthly(fx_daily: pd.DataFrame) -> pd.DataFrame:
    """Aggregate daily FX to monthly"""
    monthly = (
        fx_daily.set_index("date")
        .resample("ME")
        .agg(
            usd_rub_mean=("usd_rub", "mean"),
            usd_rub_last=("usd_rub", "last"),
            usd_rub_min=("usd_rub", "min"),
            usd_rub_max=("usd_rub", "max"),
        )
        .reset_index()
    )
    return monthly

def build_macro_dataset(gdp_file, ruonia_file, rate_infl_file, fx_file) -> tuple[pd.DataFrame, dict]:
    """Build comprehensive monthly macroeconomic dataset"""
    # Load individual datasets
    gdp_q = load_gdp_quarterly(gdp_file)
    gdp_m = convert_quarterly_to_monthly_ffill(gdp_q)
    ruonia_daily = load_ruonia_daily(ruonia_file)
    ruonia_m = aggregate_ruonia_monthly(ruonia_daily)
    rate_infl_m = load_rate_inflation_monthly(rate_infl_file)
    fx_daily = load_fx_daily(fx_file)
    fx_m = aggregate_fx_monthly(fx_daily)

    # Create common monthly index
    start_date = min(x["date"].min() for x in [gdp_m, ruonia_m, rate_infl_m, fx_m] if not x.empty)
    end_date = max(x["date"].max() for x in [gdp_m, ruonia_m, rate_infl_m, fx_m] if not x.empty)

    base = pd.DataFrame({"date": pd.date_range(start_date, end_date, freq="ME")})

    # Merge all datasets
    final = (
        base.merge(gdp_m, on="date", how="left")
            .merge(ruonia_m, on="date", how="left")
            .merge(rate_infl_m, on="date", how="left")
            .merge(fx_m, on="date", how="left")
            .sort_values("date")
            .reset_index(drop=True)
    )

    # Forward fill quarterly GDP
    for col in ["gdp_real_sa", "gdp_qoq", "gdp_yoy"]:
        if col in final.columns:
            final[col] = final[col].ffill()

    # Calculate derived features
    if "usd_rub_last" in final.columns:
        final["usd_rub_mom"] = final["usd_rub_last"].pct_change()
        final["usd_rub_yoy"] = final["usd_rub_last"].pct_change(12)

    if "ruonia_mean" in final.columns and "key_rate" in final.columns:
        final["ruonia_key_spread"] = final["ruonia_mean"] - final["key_rate"]

    if "inflation_yoy" in final.columns and "inflation_target" in final.columns:
        final["inflation_gap"] = final["inflation_yoy"] - final["inflation_target"]

    if "key_rate" in final.columns and "inflation_yoy" in final.columns:
        final["real_key_rate_ex_post"] = final["key_rate"] - final["inflation_yoy"]

    # Add lags for regression analysis
    lag_cols = ["key_rate", "inflation_yoy", "inflation_gap", "ruonia_mean", "usd_rub_last", "gdp_real_sa", "gdp_qoq", "gdp_yoy"]
    for col in lag_cols:
        if col in final.columns:
            final[f"{col}_lag1"] = final[col].shift(1)

    # Filter from Feb 2014 onwards
    cutoff_date = pd.Timestamp("2014-02-14")
    final = final[final["date"] >= cutoff_date].copy()
    final = final.reset_index(drop=True)

    meta = {
        "gdp_quarterly": gdp_q,
        "gdp_monthly": gdp_m,
        "ruonia_daily": ruonia_daily,
        "ruonia_monthly": ruonia_m,
        "rate_infl_monthly": rate_infl_m,
        "fx_daily": fx_daily,
        "fx_monthly": fx_m,
    }

    return final, meta

